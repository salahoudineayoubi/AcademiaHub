    private void supp_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supp_buttonActionPerformed
        // TODO add your handling code here:
        try {
            Connection cn = JavaConnectDb.ConnecrDb();
            Statement myStmt = cn.createStatement();
            ResultSet myRs = myStmt.executeQuery("select * from etudiant");
            
            if (matricule.getText().equals("")){
                int matri = 0;
                int i = 0;
                while(myRs.next()){
                    if(nom.getText().equals(myRs.getString("nom_etu"))){
                        matri = myRs.getInt("matricule_etu");
                        i++;
                    }}
                
                PreparedStatement st1 = cn.prepareStatement("DELETE FROM etudiantunite WHERE matricule_etu ="+Integer.toString(matri));
               
                if (i>1){
                    exist_pane.setVisible(true);
                }else{
                    st1.executeUpdate();

                    nom.setText("");
                    supp_button.setEnabled(false);
                    if (i == 0){
                        JOptionPane.showMessageDialog(this,"le nom n'existe pas ! ");
                    }else{
                        JOptionPane.showMessageDialog(this,"deleted successfully");
                    }
                }
            }else{

                exist_pane.setVisible(false);
                PreparedStatement st2 = cn.prepareStatement("DELETE FROM etudiantunite WHERE matricule_etu=?");
                st2.setString(1,matricule.getText());
                Statement myStmt2 = cn.createStatement();
                ResultSet myRs2 = myStmt.executeQuery("select * from etudiantunite where matricule_etu ="+matricule.getText());
                if (myRs2.next()){
                    st2.executeUpdate();
                JOptionPane.showMessageDialog(this,"deleted successfully");
                matricule.setText("");
                nom.setText("");
                }else{
                    JOptionPane.showMessageDialog(this,"La matricule n'existe pas dans ce tableau");
                }
                
            }
            refresh();

        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e);
        }

    }//GEN-LAST:event_supp_buttonActionPerformed

